async function seedDatabase() {
  try {
    const uri = process.env.MONGO_URI || 'mongodb://127.0.0.1:27017/nutrisetu';
    await mongoose.connect(uri);
    console.log('MongoDB connected.');

    await Food.deleteMany({});
    console.log('Cleared existing food entries.');

    const BATCH = 200;
    let inserted = 0;
    for (let i = 0; i < foodDatabase.length; i += BATCH) {
      await Food.insertMany(foodDatabase.slice(i, i + BATCH), { ordered: false });
      inserted += Math.min(BATCH, foodDatabase.length - i);
      process.stdout.write('\r   Inserting... ' + inserted + ' / ' + foodDatabase.length);
    }
    console.log('\n');

    const breakdown = {};
    foodDatabase.forEach(f => { breakdown[f.category] = (breakdown[f.category] || 0) + 1; });

    console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
    console.log('✅  SUCCESS: ' + foodDatabase.length + ' Indian food items inserted');
    console.log('📊  Breakdown:');
    Object.entries(breakdown).forEach(([cat, count]) => {
      console.log('    ' + cat.padEnd(12) + ' → ' + count + ' items');
    });
    console.log('📌  Source: IFCT 2017 / NIN India nutritional values');
    console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');

  } catch (err) {
    console.error('❌ Seeding error:', err.message);
    process.exitCode = 1;
  } finally {
    await mongoose.connection.close();
    console.log('MongoDB connection closed.');
  }
}

seedDatabase();
